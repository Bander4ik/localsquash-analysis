"""
Модуль аналізу метаданих медіафайлів для LocalSquash.

Тут будуть реалізовані функції для ЛР03:
- розрахунок коефіцієнта стиснення;
- вибір оптимального формату на основі типу контенту;
- валідація параметрів компресії.

ЦЕ ЗАГЛУШКА — повна реалізація буде у межах ЛР03.
"""

def compression_ratio(original_size: int, compressed_size: int) -> float:
    """Розрахувати коефіцієнт стиснення."""
    if original_size <= 0:
        raise ValueError("Розмір оригіналу має бути > 0")
    return 1 - (compressed_size / original_size)
